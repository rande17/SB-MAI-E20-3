public class IntersectLines {
    
    double xa;
    double ya;
    double xb;        
    double yb;
    double xc;                
    double yc;        
    double xd;        
    double yd;    

    public IntersectLines(double xa, double ya, double xb, double yb, double xc, double yc, double xd, double yd) {
        this.xa = xa;
        this.ya = ya;
        this.xb = xb;
        this.yb = yb;
        this.xc = xc;
        this.yc = yc;
        this.xd = xd;
        this.yd = yd;
    }
    
    
    
    
    
}
